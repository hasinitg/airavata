package org.apache.airavata.web.sample;

/**
 * This holds the configuration of the web application.
 */
public class WebAppConfig {
    private String appName;
    private String consumerKey;
    private String consumerSecret;
    private String authzServerURL;
    private String callbackURL;
    private String trustStorePath;
    private String trustStorePassword;

    private static WebAppConfig webAppConfig;

    public static WebAppConfig getInstance() {
        if (webAppConfig == null) {
            synchronized (WebAppConfig.class) {
                if(webAppConfig == null){
                    webAppConfig = new WebAppConfig();
                }
            }
        }
        return webAppConfig;
    }

    private WebAppConfig() {

    }

    public String getAppName() {
        return appName;
    }

    public void setAppName(String appName) {
        this.appName = appName;
    }

    public String getConsumerKey() {
        return consumerKey;
    }

    public void setConsumerKey(String consumerKey) {
        this.consumerKey = consumerKey;
    }

    public String getConsumerSecret() {
        return consumerSecret;
    }

    public void setConsumerSecret(String consumerSecret) {
        this.consumerSecret = consumerSecret;
    }

    public String getAuthzServerURL() {
        return authzServerURL;
    }

    public void setAuthzServerURL(String authzServerURL) {
        this.authzServerURL = authzServerURL;
    }

    public String getCallbackURL() {
        return callbackURL;
    }

    public void setCallbackURL(String callbackURL) {
        this.callbackURL = callbackURL;
    }

    public String getTrustStorePath() {
        return trustStorePath;
    }

    public void setTrustStorePath(String trustStorePath) {
        this.trustStorePath = trustStorePath;
    }

    public String getTrustStorePassword() {
        return trustStorePassword;
    }

    public void setTrustStorePassword(String trustStorePassword) {
        this.trustStorePassword = trustStorePassword;
    }
}
